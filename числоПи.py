# #####Вычислить число c заданной точностью d
# Пример:
# - при $d = 0.001, π = 3.141.$    $10^{-1} ≤ d ≤10^{-10}$

d = input('Укажите точность в диапазоне 10^{-1} ≤ d ≤10^{-10\n: ')
n = abs(len(d) - d.find('.') - 1)
res = 0
for i in range(n):
    res += (1 / 16**i) * ((4 / (8*i+1)) - (2 / (8*i + 4)) - (1 / (8*i + 5)) - (1 / (8*i + 6)))
print(str(res)[:len(d)])